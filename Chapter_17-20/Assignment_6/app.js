// a. Counting
console.log("Counting:");
for (var i = 1; i <= 15; i++) {
    document.write(i + ", " );
}

// b. Reverse counting
console.log("Reverse Counting:");
for (var i = 10; i >= 1; i--) {
    document.write(i + ", ");
}

// c. Even
console.log("Even:");
for (var i = 0; i <= 20; i += 2) {
    document.write(i + ", ");
}

// d. Odd
console.log("Odd:");
for (var i = 1; i <= 19; i += 2) {
    document.write(i + ", ");
}

// e. Series
console.log("Series:");
for (var i = 2; i <= 20; i += 2) {
    document.write(i + "k, ");
}
